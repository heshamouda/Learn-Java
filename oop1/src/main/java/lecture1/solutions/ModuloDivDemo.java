package lecture1.solutions;

class ModuloDivDemo
{
  public static void main( String args[] )
  {
    System.out.println( "5%3 = " + (5%3) );      
    System.out.println( "5/3 = " + (5/3) );      
    System.out.println( "5%-3 = " + (5%-3) );    
    System.out.println( "5/-3 = " + (5/-3) );    
    System.out.println( "-5%3 = " + (-5%3) );   
    System.out.println( "-5/3 = " + (-5/3) );   
    System.out.println( "-5%-3 = " + (-5%-3) );  
    System.out.println( "-5/-3 = " + (-5/-3) );  
  }
}

