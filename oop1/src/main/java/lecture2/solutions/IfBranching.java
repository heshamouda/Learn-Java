package lecture2.solutions;

public class IfBranching {
    public static void main(String[] args) {
        int a = 10;
        if (a>0){
            System.out.print("0");
        }
        if (a<10) {
            System.out.print("1");
        } else if (a<6) {
            System.out.print("2");
        } else {
            System.out.print("3");
        }

    }
}
